package com.nwsuaf.entity;

public class CiYun {
	String key;
	Double value;
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public Double getValue() {
		return value;
	}
	public void setValue(Double value) {
		this.value = value;
	}
	@Override
	public String toString() {
		return "CiYun [key=" + key + ", value=" + value + "]";
	}
	
	

}
